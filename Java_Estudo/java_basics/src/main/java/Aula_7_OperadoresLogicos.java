

public class Aula_7_OperadoresLogicos {

    /**
    Operadores Logicos:
     && = AND/E
     || = OR/OU
     */

    public static void main(String[] args) {

        var idade = 16;
        var salario = 2000.01f;

        System.out.println(idade >= 18 || salario >= 2000);

        System.out.print(idade >= 18 && salario >= 2000);

    }

}
